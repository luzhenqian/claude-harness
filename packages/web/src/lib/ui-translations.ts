export function getArticleTag(locale: Locale, order: number): { tag: string; tagClass: string } {
  const tagClasses: Record<number, string> = {
    1: 'tag-arch', 2: 'tag-perf', 3: 'tag-agent', 4: 'tag-sec',
    5: 'tag-sec', 6: 'tag-mem', 7: 'tag-perf', 8: 'tag-arch', 9: 'tag-mem',
  };
  const tag = t(locale, `tag.${order}`) !== `tag.${order}` ? t(locale, `tag.${order}`) : t(locale, 'tag.default');
  const tagClass = tagClasses[order] || 'tag-sec';
  return { tag, tagClass };
}

export function getTagLabel(locale: Locale, tag: string): string {
  if ((PASSTHROUGH_TAG_LABELS as readonly string[]).includes(tag)) return tag;
  const key = `tagLabel.${tag}`;
  const result = t(locale, key);
  return result === key ? tag : result;
}

export function formatTemplate(template: string, vars: Record<string, string | number>): string {
  let result = template;
  for (const [k, v] of Object.entries(vars)) {
    result = result.replace(`{${k}}`, String(v));
  }
  return result;
}

